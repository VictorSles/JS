// Abaixo é o uso basico de variaveis compostas (vetores ou Array)

var num = [1,2,3,4,5,6,7,8]
console.log(`Nosso vetor é: ${num}`)

// Abaixo serve para SUBSTITUIR um dos valores da variavel

num[3]=6

// Abaixo serve para ACRESCENTAR um valor a mais na variável

num.push[9]

// Abaixo serve para CONTAR a quantidade de variáveis

num.length

// Abaixo posso ORDENAR as variáveis caso nao estejam ordenadas

num.sort()

// Abaixo vemos como ENCONTRAR A POSICAO de determinado valor e armazena-lo em uma varialvel qualquer

var variavel = num.indexOf(3)
console.log(`O valor está na posicao ${variavel}`)
